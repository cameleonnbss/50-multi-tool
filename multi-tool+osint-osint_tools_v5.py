def spinner(label, dur=1.0, col=LG):
    frames = ["вЈҫ","вЈҪ","вЈ»","вўҝ","вЎҝ","вЈҹ","вЈҜ","вЈ·"]
    end = time.time() + dur
    i = 0
    while time.time() < end:
        sys.stdout.write(col + f"\r  {frames[i%8]}  {label}   ")
        sys.stdout.flush()
        time.sleep(0.07)
        i += 1
    sys.stdout.write("\r" + " "*70 + "\r")
    sys.stdout.flush()

def loading_bar(label, steps=35, delay=0.022, col=LG, bc=G):
    for i in range(steps+1):
        f = "в–Ҳ"*i + "в–‘"*(steps-i)
        pct = int(i/steps*100)
        sys.stdout.write(col + f"\r  {label}  " + bc + f"[{f}]" + LW + f" {pct:3d}%")
        sys.stdout.flush()
        time.sleep(delay)
    print()

def glitch(text, rounds=4, col=LG):
    noise = "@#$%&?!*~^<>|"
    for _ in range(rounds):
        g = "".join(random.choice(noise) if random.random()<0.25 else ch for ch in text)
        sys.stdout.write("\r" + col + g)
        sys.stdout.flush()
        time.sleep(0.06)
    sys.stdout.write("\r" + col + text + "\n")
    sys.stdout.flush()

