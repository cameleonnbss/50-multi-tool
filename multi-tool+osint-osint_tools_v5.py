def password_tools():
    show_logo(LOGO_PASS, "PASSWORD TOOLS", LM)
    print(LW + "  1 > Generate secure password")
    print(LW + "  2 > Test password strength + HIBP check")
    print(LW + "  3 > Generate multiple passwords (bulk)")
    print()
    c = input(LM + "  Choice > ").strip()

    if c == "1":
        section("PASSWORD GENERATOR", LM)
        try:    length = int(input(LM + "  Length (default 20) > ").strip() or "20")
        except: length = 20
        use_upper  = input(LM + "  Uppercase?  [Y/n] > ").strip().lower() != "n"
        use_lower  = input(LM + "  Lowercase?  [Y/n] > ").strip().lower() != "n"
        use_digits = input(LM + "  Digits?     [Y/n] > ").strip().lower() != "n"
        use_syms   = input(LM + "  Symbols?    [Y/n] > ").strip().lower() != "n"
        pool = ""
        if use_upper:  pool += string.ascii_uppercase
        if use_lower:  pool += string.ascii_lowercase
        if use_digits: pool += string.digits
        if use_syms:   pool += "!@#$%^&*()-_=+[]{}|;:,.<>?"
        if not pool:   pool = string.ascii_letters + string.digits
        pwd = "".join(random.choices(pool, k=length))
        print()
        rainbow("  " + pwd)
        print()
        row("Length",  len(pwd),  LM, LW)
        row("Pool",    len(pool), LM, LW)

    elif c == "2":
        pwd = input(LM + "  Password > ").strip()
        if not pwd: pause(); return
        section("STRENGTH ANALYSIS", LM)
        checks = [
            (len(pwd) >= 8,   "Length >= 8"),
            (len(pwd) >= 12,  "Length >= 12"),
            (len(pwd) >= 16,  "Length >= 16"),
            (any(ch.isupper() for ch in pwd), "Contains uppercase"),
            (any(ch.islower() for ch in pwd), "Contains lowercase"),
            (any(ch.isdigit() for ch in pwd), "Contains digits"),
            (any(ch in "!@#$%^&*()-_=+[]{}|;:,.<>?" for ch in pwd), "Contains special chars"),
            (not any(pwd.lower().startswith(w) for w in
                     ["password","pass","123","qwerty","admin","letme","abc","azerty"]),
             "Not a common prefix"),
        ]
        score = 0
        for passed, label in checks:
            flag = LG + "  [вң”]" if passed else LR + "  [вңҳ]"
            print(flag + LW + f"  {label}")
            if passed: score += 1
        rating = ["Very Weak","Weak","Weak","Medium","Medium","Good","Good","Strong","Very Strong"]
        r_col  = [LR,LR,LR,LY,LY,LG,LG,LG,LG][min(score,8)]
        bar    = LG+"в–Ҳ"*score + DIM+"в–‘"*(len(checks)-score)
        print()
        print(LM + f"  Score: " + bar + LM + f"  {score}/{len(checks)}  " + r_col + rating[min(score,8)])
        section("HIBP CHECK (k-anonymity вҖ” password not sent)", LM)
        sha1 = hashlib.sha1(pwd.encode()).hexdigest().upper()
        prefix, suffix = sha1[:5], sha1[5:]
        spinner("Querying HIBP", 0.8, LM)
        try:
            r = requests.get(f"https://api.pwnedpasswords.com/range/{prefix}", timeout=8)
            found = False
            for line in r.text.splitlines():
                h, count = line.split(":")
                if h == suffix:
                    print(LR + f"  [!!!]  Seen {count} times in breaches. Change it!")
                    found = True
                    break
            if not found:
                print(LG + "  [вң“]  Not found in HIBP database.")
        except Exception as e:
            print(LR + f"  HIBP check failed: {e}")

    elif c == "3":
        try:
            n = int(input(LM + "  How many? > ").strip() or "10")
            l = int(input(LM + "  Length?   > ").strip() or "20")
        except:
            n, l = 10, 20
        pool = string.ascii_letters + string.digits + "!@#$%^&*-_=+"
        section(f"{n} GENERATED PASSWORDS", LM)
        for i in range(n):
            pwd = "".join(random.choices(pool, k=l))
            rainbow(f"  {i+1:>3}.  {pwd}")

    pause()


