def intro_animation():
    clear()

    # 1 вҖ” matrix rain
    matrix_rain(7, 72)
    time.sleep(0.1)
    clear()

    # 2 вҖ” skull flicker
    skull_colors = [LG, G, LC, LG, LM]
    for col in skull_colors:
        clear()
        print(col + SKULL)
        time.sleep(0.15)

    clear()

    # 3 вҖ” boot frames
    for frame in HACK_FRAMES:
        clear()
        matrix_rain(3, 72)
        print(LG + frame)
        time.sleep(0.4)

    time.sleep(0.2)
    clear()

    # 4 вҖ” camzzz name glitch + rainbow
    matrix_rain(2, 72)
    print()
    print(LG + "  " + "в•җ"*68)
    for _ in range(6):
        noise = "@#$%camzzz&?!OSINT*~tool^<>V5"
        line = "  " + "".join(random.choice(noise) for _ in range(50))
        sys.stdout.write(random.choice([G,LG,C,LC,M,LM]) + line + "\r")
        sys.stdout.flush()
        time.sleep(0.07)
    glitch("             B Y   C A M Z Z Z   вҖ”   O S I N T   T O O L   V 5", 5, LG)
    print(LG + "  " + "в•җ"*68)
    time.sleep(0.2)

    # 5 вҖ” rainbow big camzzz name
    print()
    for line in CAMZZZ_BIG.split("\n"):
        rainbow(line)
        time.sleep(0.04)
    time.sleep(0.3)

    # 6 вҖ” user's camzzz ASCII scrolled in
    print()
    cols = [LG, LC, LM, LY, LG]
    for i, line in enumerate(CAMZZZ_ASCII.split("\n")):
        print(cols[i % len(cols)] + line)
        time.sleep(0.06)
    time.sleep(0.3)

    # 7 вҖ” loading bars per module
    print()
    bars = [
        ("  [ NETWORK MODULE    ]", LG,  G),
        ("  [ PHONE MODULE      ]", LC,  C),
        ("  [ BREACH MODULE     ]", LR,  R),
        ("  [ OSINT MODULE      ]", LM,  M),
        ("  [ INTEL MODULE      ]", LY,  Y),
        ("  [ CAMERA MODULE     ]", LR,  R),
        ("  [ PASSWORD MODULE   ]", LM,  M),
        ("  [ ALL SYSTEMS       ]", LG,  G),
    ]
    for label, col, bc in bars:
        loading_bar(label, 28, 0.015, col, bc)

    print()
    blink("  в—Ҳв—Ҳв—Ҳ  ALL SYSTEMS ONLINE  В·  By camzzz  В·  V5  в—Ҳв—Ҳв—Ҳ", 3, LG)
    time.sleep(0.3)

    # 8 вҖ” final matrix + clear
    clear()
    matrix_rain(5, 72)
    time.sleep(0.2)
    clear()


