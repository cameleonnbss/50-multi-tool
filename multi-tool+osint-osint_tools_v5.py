def rainbow(text):
    cols = [LR, Y, LG, LC, LM, LW]
    out = "".join(cols[i%len(cols)]+ch for i,ch in enumerate(text))
    print(out)

def blink(text, times=3, col=LG):
    for _ in range(times):
        sys.stdout.write("\r" + col + text); sys.stdout.flush(); time.sleep(0.28)
