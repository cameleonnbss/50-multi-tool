def wayback_urls():
    show_logo(LOGO_DNS, "WAYBACK URL EXTRACTOR", LG)
    domain = input(LG + "  Domain > ").strip()
    if not domain: pause(); return
    spinner("Querying CDX API", 1.5, LG)
    try:
        url = f"http://web.archive.org/cdx/search/cdx?url=*.{domain}&output=text&fl=original&collapse=urlkey&limit=100"
        r = requests.get(url, timeout=15)
        urls = list(set(r.text.strip().split("\n")))
        section(f"FOUND {len(urls)} UNIQUE URLs", LG)
        for u in sorted(urls)[:80]:
            print(LG + f"  {u}")
        if len(urls) > 80:
            print(LG + f"\n  ... and {len(urls)-80} more.")
    except Exception as e:
        print(LR + f"  Failed: {e}")
    pause()

