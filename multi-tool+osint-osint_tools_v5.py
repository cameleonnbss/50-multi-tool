    if not number:
        pause(); return

    spinner("Analysing number", 1.0, LC)

    info = None
    matched = ""
    for prefix in sorted(PHONE_DATA.keys(), key=lambda x: -len(x)):
        if number.startswith(prefix):
            info = PHONE_DATA[prefix]
            matched = prefix
            break

    digits = re.sub(r"\D", "", number)

    section("PHONE NUMBER ANALYSIS", LC)
    row("Full number",    number,        LC, LW)
    row("Digits only",   digits,        LC, LW)
    row("Digit count",   len(digits),   LC, LW)
    row("Country code",  matched or "Unknown", LC, LW)

    if info:
        row("Country",   info["country"], LC, LG)
        row("Region",    info["region"],  LC, LG)
        row("Timezone",  info["tz"],      LC, LG)
        row("Format",    info["format"],  LC, LY)
        row("Type",      info["type"],    LC, LW)
    else:
        row("Country",   "Not in database", LC, LR)

    # France extra info
    if matched == "+33":
        section("FRANCE DETAILS", LC)
        local = number[3:]
        if local.startswith("0"):
            local = local[1:]
        p2 = "0" + local[0] if local else ""
        fr_type = FR_PREFIXES.get(p2, "Unknown")
        row("Local format", "0" + local, LC, LW)
        row("Line type",    fr_type,     LC, LG)
        is_mobile = p2 in ["06","07"]
        row("Is mobile",    "Yes" if is_mobile else "No", LC, LG if is_mobile else LY)
        if is_mobile:
            print(LC + "\n  Possible operators: Orange, SFR, Bouygues, Free Mobile, MVNO")

