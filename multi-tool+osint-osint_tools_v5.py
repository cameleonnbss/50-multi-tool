def whois_reverse():
    show_logo(LOGO_DNS, "WHOIS / REVERSE DNS", LY)
    print(LY + "  1 > WHOIS Domain Lookup\n  2 > Reverse DNS (IP -> Hostname)\n")
    c = input(LY + "  Choice > ").strip()
    if c=="1":
        d = input(LY + "  Domain > ").strip()
        spinner("Querying WHOIS", 1.0, LY)
        try:
            r = requests.get(f"https://api.whois.vu/?q={d}", timeout=10).json()
            section("WHOIS DATA", LY)
            for k, v in r.items():
                if v: row(k, v, LY, LW)
        except:
            try:
                ip = socket.gethostbyname(d)
                row("IP", ip, LY, LW)
            except Exception as e:
                print(LR + f"  Failed: {e}")
    elif c=="2":
        ip = input(LY + "  IP > ").strip()
        spinner("Reverse DNS", 0.8, LY)
        try:
            h = socket.gethostbyaddr(ip)
            section("REVERSE DNS", LY)
            row("IP", ip, LY, LW); row("Hostname", h[0], LY, LG)
            if h[1]: row("Aliases", ", ".join(h[1]), LY, LW)
        except Exception as e:
            print(LR + f"  Failed: {e}")
    pause()


