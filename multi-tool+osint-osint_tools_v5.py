def username_tracker():
    show_logo(LOGO_USER, "USERNAME TRACKER", LM)
    username = input(LM + "  Username > ").strip()
    if not username: pause(); return
    loading_bar("  Preparing scan", 25, 0.015, LM, M)
    print(LM + f"\n  Checking {len(SITES)} platforms for '{username}'...\n")
    found = []
    def check(item):
        name, tpl = item
        url = tpl.format(username)
        try:
            r = requests.get(url, timeout=8, allow_redirects=True,
                             headers={"User-Agent":"Mozilla/5.0"})
            return ("FOUND" if r.status_code==200 else "В·", name, url)
        except:
            return ("ERR", name, url)
    with concurrent.futures.ThreadPoolExecutor(max_workers=20) as ex:
        results = list(ex.map(check, SITES))
    for status, name, url in results:
        if status=="FOUND":
            print(LG + f"  [+]  {name:<22}" + LW + f" {url}")
            found.append(url)
        elif status=="ERR":
            print(DIM+G + f"  [?]  {name}")
        else:
            print(DIM+G + f"  [ ]  {name}")
    section(f"RESULTS  вҖ”  {len(found)} FOUND  /  {len(SITES)} CHECKED", LM)
    pause()


